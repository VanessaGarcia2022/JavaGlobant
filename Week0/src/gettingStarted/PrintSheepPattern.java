package gettingStarted;

/**
 * Exercise1: Write a program called gettingStarted.exercise1.PrintSheepPattern to print the following pattern
 *            '__'
 *           (oo)
 *    /========//
 *   / || @@ ||
 *  *  ||----||
 *     VV    VV
 *     ''    ''
 * */
public class PrintSheepPattern {
    public static void main(String[] args) {
        System.out.println("          '__'");
        System.out.println("          (oo)");
        System.out.println("  /========//");
        System.out.println(" / || @@ ||");
        System.out.println("*  ||----||");
        System.out.println("   VV    VV");
        System.out.println("   ''    ''");
    }
}
